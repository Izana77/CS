package t;

public class Q1 {
	public static void main(String[] args) {
	CalculateExpression (10, 9, 8, 7);
	
		
	}
	public static double CalculateExpression(double n, double x, double y, double z) {
	    double sumSquares = (x * x) + (y * y) + (z * z);
	    return 7.2 * n * sumSquares;
}
}